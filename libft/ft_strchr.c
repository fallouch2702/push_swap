#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if ((int)s[i] == c)
			return (((char*)s + i));
		i++;
	}
	if (c == 0)
		return (((char*)s + i));
	return (0);
}
