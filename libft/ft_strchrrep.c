int		ft_strchrrep(char *str, char c)
{
	int		i;

	i = 0;
	if (!str)
		return (0);
	if (!c)
		return (1);
	while (*str)
	{
		if (*str == c)
			i++;
		str++;
	}
	return (i);
}
