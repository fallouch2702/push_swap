char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int j;
	int l;

	i = 0;
	j = 0;
	l = 0;
	while (s2[l] != '\0')
		l++;
	if (l == 0)
		return ((char *)s1);
	while (s1[i])
	{
		while (s2[j] == s1[i + j])
		{
			if (j == l - 1)
				return ((char *)s1 + i);
			j++;
		}
		j = 0;
		i++;
	}
	return (0);
}
